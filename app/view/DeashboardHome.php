    <script>
        function change_color(){
            let elements_color = document.getElementsByClassName('colorpciker');
            let elements_bg = document.getElementsByClassName('colorpciker_bg');
            let picker = document.getElementById('picker');

            setInterval(() => {
                for(let i = 0; i < elements_color.length; i++){
                    elements_color[i].style.color = picker.value;
                }

                for(let i = 0; i < elements_bg.length; i++){
                    elements_bg[i].style.backgroundColor = picker.value;
                }
            }, 100)
        }

        change_color();
    </script>

